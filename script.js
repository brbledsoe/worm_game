$(document).keydown(function(e) {
  switch(e.which) {
   case 37: // left
   moveLeft();
   break;

   case 38: // up
   moveUp();
   break;

   case 39: // right
   moveRight();
   break;

   case 40: // down
   moveDown();
   break;

   default: return; // exit this handler for other keys
  }

  e.preventDefault(); // prevent the default action (scroll / move caret)
});


